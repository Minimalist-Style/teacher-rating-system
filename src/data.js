export const calculateScore = (teacher) => {
  const { school, district, regional, republican, international } = teacher.awards;
  return (school * awardWeights.school) + 
         (district * awardWeights.district) + 
         (regional * awardWeights.regional) + 
         (republican * awardWeights.republican) +
         (international * awardWeights.international);
};

// Helper for status colors
export const getStatusColor = (score) => {
  if (score >= 15) return 'green';
  if (score >= 6) return 'yellow';
  return 'red';
};
